package com.akjava.wiki.client.ecs;

public class IMG {
private String alt;
private int border;
private String src;
public String getAlt() {
	return alt;
}
public IMG setAlt(String alt) {
	this.alt = alt;
	return this;
}
public int getBorder() {
	return border;
}
public IMG setBorder(int border) {
	this.border = border;
	return this;
}
public String getSrc() {
	return src;
}
public IMG setSrc(String src) {
	this.src = src;
	return this;
}
public String toString(){
	return "<img src='"+src+"' border="+border+" alt="+alt+">"; 
}
}
